# Leia orçamento (float) e gasto (float). Faça orcamento -= gasto.
# Solicite ao usuário que informe um orçamento (float) e um gasto (float). Utilize atribuição -= para descontar o gasto do orçamento. 

print ("Digite o orçamento: ")
orcamento = float(input())
print ("Digite o gasto: ")
gasto = float(input())
orcamento -= gasto
print ("O orçamento restante é: ", orcamento)

