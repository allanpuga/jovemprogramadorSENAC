# 1. Crie uma lista vazia
lista_compras = []
print(lista_compras) # Exibe a lista vazia

# 2 & 3. Utilize append() para adicionar itens e print() para exibir
lista_compras.append("pão")
print(lista_compras)

lista_compras.append("leite")
print(lista_compras)

# 4. Utilize remove() para remover um item e print() para exibir
lista_compras.remove("pão")
print(lista_compras)